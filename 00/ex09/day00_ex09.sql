SELECT
	(
		SELECT
			NAME
		FROM
			PERSON
		WHERE
			ID = PV.PERSON_ID
	) AS PERSON_NAME,
	(
		SELECT
			NAME
		FROM
			PIZZERIA
		WHERE
			ID = PV.PIZZERIA_ID
	) AS PIZZERIA_NAME
FROM
	(
		SELECT
			PERSON_ID,
			PIZZERIA_ID
		FROM
			PERSON_VISITS
		WHERE
			VISIT_DATE BETWEEN '2022-01-07' AND '2022-01-09'
	) AS PV
ORDER BY
	PERSON_NAME ASC,
	PIZZERIA_NAME DESC;