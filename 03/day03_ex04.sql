WITH
	WHO_VISIT_TABLE AS (
		SELECT
			PIZZERIA.NAME AS PIZZERIA_NAME,
			GENDER
		FROM
			PERSON_ORDER
			JOIN MENU ON MENU_ID = MENU.ID
			JOIN PERSON ON PERSON_ID = PERSON.ID
			JOIN PIZZERIA ON MENU.PIZZERIA_ID = PIZZERIA.ID
	)
SELECT
	PIZZERIA_NAME
FROM
	(
		(
			SELECT
				PIZZERIA_NAME
			FROM
				WHO_VISIT_TABLE
			WHERE
				GENDER = 'male'
			EXCEPT
			SELECT
				PIZZERIA_NAME
			FROM
				WHO_VISIT_TABLE
			WHERE
				GENDER = 'female'
		)
		UNION
		(
			SELECT
				PIZZERIA_NAME
			FROM
				WHO_VISIT_TABLE
			WHERE
				GENDER = 'female'
			EXCEPT
			SELECT
				PIZZERIA_NAME
			FROM
				WHO_VISIT_TABLE
			WHERE
				GENDER = 'male'
		)
	)
ORDER BY
	PIZZERIA_NAME;