WITH
	CTE_FEMALE AS (
		SELECT
			*
		FROM
			PERSON_ORDER
			JOIN PERSON ON PERSON.ID = PERSON_ID
			JOIN MENU ON MENU_ID = MENU.ID
		WHERE
			GENDER = 'female'
	)
SELECT
	NAME
FROM
	CTE_FEMALE
WHERE
	PIZZA_NAME = 'pepperoni pizza'
INTERSECT
SELECT
	NAME
FROM
	CTE_FEMALE
WHERE
	PIZZA_NAME = 'cheese pizza'
ORDER BY
	NAME;